CREATE TABLE IF NOT EXISTS invoices.invoices (
	id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	invoice_id varchar(250),
	invoice_type varchar(250),
	contact_id varchar(250),
	invoice_date date,
	invoice_due_date date,
	line_amount_types varchar(250),
  invoice_number varchar(250),
  reference varchar(250),
  branding_theme_id varchar(250),
  url varchar(1000),
  currency_code varchar(250),
	currency_rate decimal,
	status varchar(250),
	sent_to_contact int,
	expected_payment_date date,
	planned_payment_date date,
	cis_deduction decimal,
	sub_total decimal,
	total_tax decimal,
	total decimal,
	total_discount decimal,
	has_attachments int,
  is_discounted int,
	amount_due decimal,
	amount_paid decimal,
	fully_paid_on_date date,
	amount_credited decimal,
	updated_date_utc timestamp,
	status_attribute_string varchar
);

create index IF NOT EXISTS invoice_id_indx on invoices.invoices (invoice_id);

